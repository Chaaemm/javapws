public class TriangleArea {
    public static void main(String[] args) {
        // กำหนดค่าฐานและสูง
        double base = 70;
        double height = 85;

        // คำนวณพื้นที่ของสามเหลี่ยม
        double area = 0.5 * base * height;

        // แสดงผลลัพธ์
        System.out.println("พื้นที่ของรูปสามเหลี่ยม = " + area + " ตารางหน่วย");
    }
}